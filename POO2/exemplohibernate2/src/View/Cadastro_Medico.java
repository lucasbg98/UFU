    private void cargoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargoActionPerformed
           
            //Verificando no campo cargo o item selecionado e atraves do fabrica abstrata criando um objeto do tipo escolhido
            if(cargo.getSelectedItem().equals("Pediatra")){
                
                this.ob=(Pediatra) ma.criarPedia();
                
            }else if(cargo.getSelectedItem().equals("Dermatologista")){
                
                this.ob=(Dermatologista)ma.criarDerma();
                
            }else if(cargo.getSelectedItem().equals("Ortopedista")){
                
                this.ob=(Ortopedista)ma.criarOrto();
                
            }else if(cargo.getSelectedItem().equals("Fisioterapeuta")){
                
                this.ob=(Fisioterapeuta)ma.criarFisio();
                
            }
        
           
    }//GEN-LAST:event_cargoActionPerformed

