    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        String aux;
        try{
            
            _Medico_ m=new _Medico_();
            //m=_Medico_DAO.getById(campopesq.getText());
            Nome.setEnabled(false);
            Masculino.setEnabled(false);
            Feminino.setEnabled(false);
            cpf.setEnabled(false);
            telefone.setEnabled(false);
            endereco.setEnabled(false);
            data.setEnabled(false);
            cargo.setEnabled(false);
            Nome.setText(m.nome);
            data.setText(m.data);
            endereco.setText(m.endereco);
            telefone.setText(m.telefone);
            cpf.setText(m.cpf);
            cargo.setSelectedItem(m.cargo);
            aux=m.sexo;
            if(aux.equals("Masculino")){
                Masculino.setSelected(true);
            }else{
                Feminino.setSelected(true);
            }
            
            int op=showConfirmDialog(null,"Deseja deletar?");
            if(op==0){
            //_Medico_DAO.excluir(m);
            JOptionPane.showMessageDialog(null,"Excluido com sucesso!");
            campopesq.setVisible(false);
            pesq.setVisible(false);
            ok.setVisible(false); 
            
            Novo.setEnabled(true);
            Salvar.setEnabled(false);
            Excluir.setEnabled(false);
            data.setText("");
            endereco.setText("");
            telefone.setText("");
            Nome.setText("");
            cpf.setText("");
            
            Masculino.setSelected(false);
            Feminino.setSelected(false);
            
            }else{
              Masculino.setSelected(false);
              Feminino.setSelected(false);
              Salvar.setEnabled(false);
              Novo.setEnabled(true);
              Excluir.setEnabled(false); 
              
              campopesq.setVisible(false);
              pesq.setVisible(false);
              ok.setVisible(false); 
              
            }
      
        }
        catch(RuntimeException e)
        {
            JOptionPane.showMessageDialog(null,"Cpf nao encontrado!");
            Salvar.setEnabled(false);
            Novo.setEnabled(true);
            Excluir.setEnabled(false);
    
   
        
        }
            
       
    }//GEN-LAST:event_okActionPerformed

