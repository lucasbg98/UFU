    public int Buscacod(int cod){
        
        for(int i=0;i<=p.contf;i++){
            
            if(p.fornecedor[i].getCodigoF()==cod){
                return 1; 
            }
        }
        
                
        return 0;
    }
    
    
    public void Exibir(int cad){
        
        try{
            
            CampoNome.setText(p.fornecedor[cad].getNomeF());
            CampoCodigo.setText(Integer.toString(p.fornecedor[cad].getCodigoF()));
           
           
        }catch(ArrayIndexOutOfBoundsException e){
            
            JOptionPane.showMessageDialog(null,"Sem informacoes para tras!");
        
            
        }catch(NullPointerException e){
            
            
            JOptionPane.showMessageDialog(null, "Sem informacoes para frente!");   
                
                
        }
        
        
        
    }
    
    
    
    
    
