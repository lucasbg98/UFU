    private void BSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSalvarActionPerformed
        if(p.contf==9){
            
            JOptionPane.showMessageDialog(null,"Limite máximo de fornecedores cadastrados!"+"\n");
            
        }else{
            
            if(Buscacod(Integer.parseInt(CampoCodigo.getText()))==1){
                
              JOptionPane.showMessageDialog(null,"O código já está sendo utilizado,informe novamente!");
              
            }else{
                
                p.contf++;
                forne=p.contf;
                p.fornecedor[p.contf]=new Fornecedor();
                p.fornecedor[p.contf].CadastrarF(Integer.parseInt(CampoCodigo.getText()),CampoNome.getText());
                FornecedorDAO.salvar(p.fornecedor[p.contf]); 
                
                
                CampoNome.setEnabled(false);
                CampoCodigo.setEnabled(false);
                
                BSalvar.setEnabled(false);
                BNovo.setEnabled(true);
                BAlterar.setEnabled(true);
                BRemover.setEnabled(true);
                BProx.setEnabled(true);
                BAnt.setEnabled(true);
                
                
               
                
                
                
                
                
            }
            
        }
    }//GEN-LAST:event_BSalvarActionPerformed

